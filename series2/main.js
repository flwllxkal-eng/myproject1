document.addEventListener('DOMContentLoaded', () => {
    // Get references to DOM elements
    const startDateInput = document.getElementById('start-date');
    const intervalInput = document.getElementById('watering-interval'); // Hidden input
    const intervalButtons = document.querySelectorAll('.interval-btn');
    const durationButtons = document.querySelectorAll('.duration-btn');
    const scheduleList = document.getElementById('schedule-list');
    const exportBtn = document.getElementById('export-calendar-btn');

    // Set default start date to today
    const today = new Date();
    startDateInput.value = today.toISOString().split('T')[0];

    let wateringEvents = [];

    // Function to calculate watering dates
    const calculateSchedule = () => {
        const startDate = new Date(startDateInput.value + 'T00:00:00'); // Use specific time to avoid timezone issues
        const interval = parseInt(intervalInput.value, 10);
        const activeDurationBtn = document.querySelector('.duration-btn.active');
        const months = parseInt(activeDurationBtn.dataset.months, 10);

        if (isNaN(startDate.getTime()) || isNaN(interval) || interval <= 0) {
            scheduleList.innerHTML = '<p class="placeholder">Please select a valid start date and interval.</p>';
            exportBtn.disabled = true;
            return;
        }

        wateringEvents = [];
        const schedule = [];
        const endDate = new Date(startDate);
        endDate.setMonth(endDate.getMonth() + months);

        let currentDate = new Date(startDate);

        while (currentDate <= endDate) {
            schedule.push(new Date(currentDate));
            wateringEvents.push({
                title: 'Water Your Plants',
                startDate: new Date(currentDate)
            });
            currentDate.setDate(currentDate.getDate() + interval);
        }

        displaySchedule(schedule);
        exportBtn.disabled = schedule.length === 0;
    };

    // Function to display the schedule in English
    const displaySchedule = (schedule) => {
        scheduleList.innerHTML = ''; // Clear previous results
        if (schedule.length === 0) {
            scheduleList.innerHTML = '<p class="placeholder">No watering dates in this period.</p>'
            return;
        }

        schedule.forEach(date => {
            const item = document.createElement('div');
            item.className = 'schedule-item';
            // Force English (US) format for the date
            item.textContent = date.toLocaleDateString('en-US', { weekday: 'long', year: 'numeric', month: 'long', day: 'numeric' });
            scheduleList.appendChild(item);
        });
    };

    // Function to generate and download an iCalendar (.ics) file
    const exportToCalendar = () => {
        if (wateringEvents.length === 0) return;

        let icsContent = [
            'BEGIN:VCALENDAR',
            'VERSION:2.0',
            'PRODID:-//flwllxkal-eng/myproject1//NONSGML v1.0//EN'
        ];

        wateringEvents.forEach(event => {
            const eventDate = event.startDate;
            const year = eventDate.getFullYear();
            const month = ('0' + (eventDate.getMonth() + 1)).slice(-2);
            const day = ('0' + eventDate.getDate()).slice(-2);
            const dateString = `${year}${month}${day}`;

            icsContent.push(
                'BEGIN:VEVENT',
                `DTSTAMP:${dateString}T000000Z`,
                `DTSTART;VALUE=DATE:${dateString}`,
                `SUMMARY:${event.title}`,
                'DESCRIPTION:Time to water your plants! Generated by Plant Watering Scheduler.',
                'END:VEVENT'
            );
        });

        icsContent.push('END:VCALENDAR');
        const icsFile = icsContent.join('\r\n');

        const blob = new Blob([icsFile], { type: 'text/calendar;charset=utf-8' });
        const link = document.createElement('a');
        link.href = URL.createObjectURL(blob);
        link.download = 'plant_watering_schedule.ics';
        document.body.appendChild(link); // Required for Firefox
        link.click();
        document.body.removeChild(link);
    };

    // --- Event Listeners ---
    startDateInput.addEventListener('change', calculateSchedule);

    intervalButtons.forEach(button => {
        button.addEventListener('click', () => {
            intervalButtons.forEach(btn => btn.classList.remove('active'));
            button.classList.add('active');
            intervalInput.value = button.dataset.interval; // Update hidden input
            calculateSchedule();
        });
    });

    durationButtons.forEach(button => {
        button.addEventListener('click', () => {
            durationButtons.forEach(btn => btn.classList.remove('active'));
            button.classList.add('active');
            calculateSchedule();
        });
    });

    exportBtn.addEventListener('click', exportToCalendar);

    // Initial calculation on page load
    calculateSchedule();
});
